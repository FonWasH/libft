#include "libft.h"

size_t	ft_strlcpy(char *d, char *s, size_t n)
{
	size_t	i;

	i = 0;
	while (s[i] && i + 1 < n)
	{
		d[i] = s[i];
		i++;
	}
	if (n)
		d[i] = '\0';
	return (ft_strlen(s));
}
