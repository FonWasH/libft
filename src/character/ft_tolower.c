#include "libft.h"

int	ft_tolower(int c)
{
	if (c == EOF || c < CHAR_MIN || c > UCHAR_MAX)
		return (c);
	return ((unsigned char)c + (32 * (c >= 'A' && c <= 'Z')));
}
