#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*tmp;
	t_list	*new;
	void	*data;

	tmp = NULL;
	while (lst)
	{
		data = f(lst->data);
		new = ft_lstnew(data);
		if (!new)
		{
			del(data);
			if (tmp)
				ft_lstclear(&tmp, del);
			return (NULL);
		}
		ft_lstadd_back(&tmp, new);
		lst = lst->next;
	}
	return (tmp);
}
